# 基础的+ - * /(加减乘除)运算
# 加法 6+6=12
print(6+6)
# 减法 6-6=0
print(6-6)
# 乘法 6*6=36
print(6*6)
# 除法 6/6=1
print(6/6)

# 平方运算 **
print(2**2) # 2的平方是4
print(2**3) # 2的立方是8
print(2**4) # 2的四次方是16

# 余数运算 %
print(8%2) # 8除以2余数为0
print(9%2) # 9除以2余数为1

# 取整运算 //
print(9//3) # 9除以3取整就是3
print(9//4) # 9除以4取整就是2

